    private void botonConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsultaActionPerformed
        try {
            // busca la cuenta y la devuelve
            String idEmp = IDEmpleadonom.getText() != null && !IDEmpleadonom.getText().isEmpty() ? IDEmpleadonom.getText(): "0";
            Empleado emp = InterfazPrincipal.TabladeEmpleados.queryForId(Integer.parseInt(idEmp));
            // Se imprime la información en el Area de Texto
            if (emp == null) {
                JOptionPane.showMessageDialog(null, "No se encuentra el empleado con el criterio de busqueda",
                        "Mensaje", JOptionPane.WARNING_MESSAGE);
                this.AreadeSalario.setText("");
                return;
            }
            // Se imprime la información en el Area de Texto
            StringBuilder sb = new StringBuilder();
            sb.append(emp.getSalariobase());
            AreadeSalario.setText(sb.toString());
            String valorSalarioBase = sb.toString();
            if (valorSalarioBase != null && !valorSalarioBase.isEmpty()) {
                this.salarioBase = Float.parseFloat(valorSalarioBase);
            } else {
                this.salarioBase = 0F;
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "El empleado no pudo ser localizado, intente nuevamente");
        }
    }//GEN-LAST:event_botonConsultaActionPerformed

    private void saldoaPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saldoaPagarActionPerformed
        /*
          Se procede a tomar las horas extras nocturnas y diurnas Calcular el
          valor a pagar por horas extras dependiendo del salario y restar las
          deducciones
         */
        String strHeDiurna = this.heDiurna.getText() != null && !this.heDiurna.getText().isEmpty() ? this.heDiurna.getText() : "0";
        String strHeNocturna = this.heNocturna.getText() != null && !this.heNocturna.getText().isEmpty() ? this.heNocturna.getText() : "0";
        Integer totalHEDiurna = Integer.parseInt(strHeDiurna);
        Integer totalHENocturna = Integer.parseInt(strHeNocturna);
        Float valorHora = this.salarioBase / 30 / 8;
        Double recargoHED = (valorHora * 0.25) + valorHora;
        Double recargoHEN = (valorHora * 0.75) + valorHora;
        Double valorCalcHEDiurna = recargoHED * totalHEDiurna;
        Double valorCalcHENocturna = recargoHEN * totalHENocturna;
        Double valorPagarHorasExtras = valorCalcHEDiurna + valorCalcHENocturna;

        Double resultadoAPagar = this.salarioBase + valorPagarHorasExtras - this.descuentosRealizados;
        this.salarioaPagar.setText(resultadoAPagar.toString());
    }//GEN-LAST:event_saldoaPagarActionPerformed

