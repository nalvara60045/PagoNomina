    public float descuento() 
    {
        String valor = JOptionPane.showInputDialog("Ingrese la cantidad a descontar");
        if (valor != null) 
        {
            int input;
            if (valor.isEmpty()) {
                input = 0;
            } else {
                input = Integer.parseInt(valor);
            }
            this.descuentosRealizados += input;
            System.out.println("La suma de descuento => " + this.descuentosRealizados);
        }

        return this.descuentosRealizados;
    }

